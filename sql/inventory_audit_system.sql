-- Inventory Audit System Database Schema
-- This file creates all necessary tables and triggers for the comprehensive inventory audit system

-- Table: inventory_movement_log
-- Stores detailed audit trail of all inventory movements
CREATE TABLE IF NOT EXISTS `inventory_movement_log` (
  `log_id` int(11) NOT NULL AUTO_INCREMENT,
  `drug_id` int(11) NOT NULL,
  `inventory_id` int(11) NOT NULL,
  `warehouse_id` varchar(31) DEFAULT NULL,
  `movement_type` varchar(50) NOT NULL,
  `quantity_before` decimal(10,2) NOT NULL DEFAULT 0.00,
  `quantity_change` decimal(10,2) NOT NULL,
  `quantity_after` decimal(10,2) NOT NULL DEFAULT 0.00,
  `reference_id` int(11) DEFAULT NULL,
  `reference_type` varchar(50) DEFAULT NULL,
  `lot_number` varchar(50) DEFAULT NULL,
  `expiration_date` date DEFAULT NULL,
  `unit_cost` decimal(10,2) DEFAULT NULL,
  `total_value` decimal(10,2) DEFAULT NULL,
  `reason` varchar(255) DEFAULT NULL,
  `notes` text DEFAULT NULL,
  `user_id` int(11) NOT NULL,
  `ip_address` varchar(45) DEFAULT NULL,
  `movement_date` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`log_id`),
  KEY `idx_drug_id` (`drug_id`),
  KEY `idx_inventory_id` (`inventory_id`),
  KEY `idx_warehouse_id` (`warehouse_id`),
  KEY `idx_movement_type` (`movement_type`),
  KEY `idx_user_id` (`user_id`),
  KEY `idx_movement_date` (`movement_date`),
  KEY `idx_reference` (`reference_type`, `reference_id`),
  KEY `idx_ip_address` (`ip_address`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Table: inventory_settings
-- Stores configuration settings for the audit system
CREATE TABLE IF NOT EXISTS `inventory_settings` (
  `setting_id` int(11) NOT NULL AUTO_INCREMENT,
  `setting_name` varchar(100) NOT NULL,
  `setting_value` text DEFAULT NULL,
  `setting_description` text DEFAULT NULL,
  `is_global` tinyint(1) NOT NULL DEFAULT 1,
  `facility_id` int(11) DEFAULT NULL,
  `created_date` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `modified_date` datetime DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`setting_id`),
  UNIQUE KEY `uk_setting_name_facility` (`setting_name`, `facility_id`),
  KEY `idx_setting_name` (`setting_name`),
  KEY `idx_is_global` (`is_global`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

