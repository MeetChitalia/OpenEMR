-- Create triggers for automatic audit logging

-- Trigger: trg_drug_sales_audit
-- Automatically logs sales transactions
DELIMITER $$
CREATE TRIGGER IF NOT EXISTS `trg_drug_sales_audit` 
AFTER INSERT ON `drug_sales` 
FOR EACH ROW 
BEGIN
    DECLARE v_user_id INT DEFAULT NULL;
    DECLARE v_facility_id VARCHAR(31) DEFAULT NULL;
    
    -- Get current user from session or default
    SET v_user_id = IFNULL(@current_user_id, 1);
    SET v_facility_id = IFNULL(@current_facility_id, 'default');
    
    -- Log the sale movement
    INSERT INTO inventory_movement_log (
        drug_id, inventory_id, warehouse_id, movement_type,
        quantity_before, quantity_change, quantity_after,
        reference_id, reference_type, lot_number, expiration_date,
        unit_cost, total_value, reason, notes, user_id, ip_address, movement_date
    ) VALUES (
        NEW.drug_id, NEW.inventory_id, NEW.warehouse_id, 'sale',
        (SELECT on_hand FROM drug_inventory WHERE inventory_id = NEW.inventory_id),
        -NEW.quantity, 
        (SELECT on_hand - NEW.quantity FROM drug_inventory WHERE inventory_id = NEW.inventory_id),
        NEW.sale_id, 'drug_sale', 
        (SELECT lot_number FROM drug_inventory WHERE inventory_id = NEW.inventory_id),
        (SELECT expiration FROM drug_inventory WHERE inventory_id = NEW.inventory_id),
        NEW.unit_price, NEW.quantity * NEW.unit_price,
        'Sale transaction', 'Automatic audit log', v_user_id, IFNULL(@current_ip_address, NULL), NOW()
    );
END$$
DELIMITER ;

-- Trigger: trg_drug_inventory_update_audit
-- Logs inventory updates
DELIMITER $$
CREATE TRIGGER IF NOT EXISTS `trg_drug_inventory_update_audit` 
AFTER UPDATE ON `drug_inventory` 
FOR EACH ROW 
BEGIN
    DECLARE v_user_id INT DEFAULT NULL;
    DECLARE v_facility_id VARCHAR(31) DEFAULT NULL;
    DECLARE v_movement_type VARCHAR(50) DEFAULT 'adjustment';
    
    -- Get current user from session or default
    SET v_user_id = IFNULL(@current_user_id, 1);
    SET v_facility_id = IFNULL(@current_facility_id, 'default');
    
    -- Determine movement type based on changes
    IF NEW.on_hand > OLD.on_hand THEN
        SET v_movement_type = 'addition';
    ELSEIF NEW.on_hand < OLD.on_hand THEN
        SET v_movement_type = 'reduction';
    END IF;
    
    -- Only log if quantity changed
    IF NEW.on_hand != OLD.on_hand THEN
        INSERT INTO inventory_movement_log (
            drug_id, inventory_id, warehouse_id, movement_type,
            quantity_before, quantity_change, quantity_after,
            reference_id, reference_type, lot_number, expiration_date,
            unit_cost, total_value, reason, notes, user_id, ip_address, movement_date
        ) VALUES (
            NEW.drug_id, NEW.inventory_id, NEW.warehouse_id, v_movement_type,
            OLD.on_hand, NEW.on_hand - OLD.on_hand, NEW.on_hand,
            NULL, 'inventory_update', NEW.lot_number, NEW.expiration,
            NEW.cost_per_unit, ABS(NEW.on_hand - OLD.on_hand) * NEW.cost_per_unit,
            'Inventory adjustment', 'Automatic audit log', v_user_id, IFNULL(@current_ip_address, NULL), NOW()
        );
    END IF;
END$$
DELIMITER ;

